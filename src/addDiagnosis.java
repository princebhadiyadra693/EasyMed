    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String patientID=jTextField1.getText();
        try{
            // JOptionPane.showMessageDialog(null, "Successfully 1");
            Connection con=ConnectionProvider.getcon();
            // JOptionPane.showMessageDialog(null, "Successfully 2");
            Statement st=con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_READ_ONLY);
            // JOptionPane.showMessageDialog(null, "Successfully 3");
            
            ResultSet rs=st.executeQuery("select *from patient where pat_id='"+patientID+"'");
            // JOptionPane.showMessageDialog(null, "Successfully 4");
           
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
           
            if(!rs.first())
            {
                // JOptionPane.showMessageDialog(null, "Successfully 5");
                jLabel2.setVisible(true);
                // JOptionPane.showMessageDialog(null, "Successfully 6");
            }
            else
            {
                // JOptionPane.showMessageDialog(null, "Successfully 7");
                jLabel2.setVisible(false);
                //  JOptionPane.showMessageDialog(null, "Successfully 8");
                jTextField1.setEditable(false);
                flag=1;
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Connection Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

