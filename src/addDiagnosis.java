    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(flag==1)
        {
            String PatientID=jTextField1.getText();
            String diagDetail=jTextField2.getText();
            String diagRemark=jTextField3.getText();
            String diagDate=jTextField4.getText();
            String wardReq,typeWard;
            if(jCheckBox1.isSelected())
            {
                wardReq="YES";
                typeWard=(String)jComboBox1.getSelectedItem();
            }
            else
            {
                wardReq="NO";
                typeWard="";
            }
            
            try{
                 Connection con=ConnectionProvider.getcon();
                 // JOptionPane.showMessageDialog(null, "Successfully 1");
                 Statement st=con.createStatement();
                 // JOptionPane.showMessageDialog(null, "Successfully 2");
                 st.executeUpdate("insert into ward values('"+PatientID+"','"+wardReq+"','"+typeWard+"')  ");
                 // JOptionPane.showMessageDialog(null, "Successfully 4");
                 st.executeUpdate("insert into patient_diagnosis values('"+PatientID+"','"+diagDate+"','"+diagDetail+"','"+diagRemark+"')  ");
                  JOptionPane.showMessageDialog(null, "Successfully Updated");
                 setVisible(false);
                 new addDiagnosis().setVisible(true);
            }
            catch(Exception e)
            {
                // System.out.println(e);
                JOptionPane.showMessageDialog(null,"Enter a data in correct form");
            }
        }
        else
        {
             JOptionPane.showMessageDialog(null,"PatientID field is empty!");
        }   
    }//GEN-LAST:event_jButton2ActionPerformed

